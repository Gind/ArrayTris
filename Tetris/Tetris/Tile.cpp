Tile::Tile()
{
	setTileType(6, 0);
}


Tile::~Tile()
{
}


int Tile::getTileType(int p_X, int p_Y)
{
	return m_Tile[p_X][p_Y];
}

void Tile::setTileType(int p_Tile, int p_Rotation)
{
	m_CurrentTileID = p_Tile;
	m_Rotation = p_Rotation;
	for (int i = 0; i < 5; i++)
	{
		for (int j = 0; j < 5; j++)
		{
			m_Tile[i][j] = m_Tiles[p_Tile][p_Rotation][j][i];
		}
	}
}

void Tile::rotateLeft()
{
	if (m_Rotation > 0)
	{
		m_Rotation--;
		for (int i = 0; i < 5; i++)
		{
			for (int j = 0; j < 5; j++)
			{
				m_Tile[i][j] = m_Tiles[m_CurrentTileID][m_Rotation][j][i];
			}
		}
	}
	else
	{
		m_Rotation = 3;
		for (int i = 0; i < 5; i++)
		{
			for (int j = 0; j < 5; j++)
			{
				m_Tile[i][j] = m_Tiles[m_CurrentTileID][m_Rotation][j][i];
			}
		}
	}
}

void Tile::rotateRight()
{
	if (m_Rotation < 3)
	{
		m_Rotation++;
		for (int i = 0; i < 5; i++)
		{
			for (int j = 0; j < 5; j++)
			{
				m_Tile[i][j] = m_Tiles[m_CurrentTileID][m_Rotation][j][i];
			}
		}
	}
	else
	{
		m_Rotation = 0;
		for (int i = 0; i < 5; i++)
		{
			for (int j = 0; j < 5; j++)
			{
				m_Tile[i][j] = m_Tiles[m_CurrentTileID][m_Rotation][j][i];
			}
		}
	}
}